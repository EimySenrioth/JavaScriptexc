let ingresa = Number(prompt('ingresa tu calificacion'));

if (ingresa >= 90){
    console.log('Excelente');
}

else if (ingresa >= 80){
    console.log('Muy bien');
}

else if (ingresa >= 70){
    console.log('Bien');
}

else if (ingresa >= 60){    
    console.log('No aprueba');
}

else if (ingresa >= 50){
    console.log('Deficiente');    
}
